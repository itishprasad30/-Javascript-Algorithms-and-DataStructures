const os = require("os");

// Os platform
console.log(os.platform());

// os arch()
console.log(os.arch());
// OS  cpu info
console.log(os.cpus());

// free memory
console.log(os.freemem());

// total memory
console.log(os.totalmem());

//home dir
console.log(os.homedir());

// uptime
console.log(os.uptime());
